let listaCompras = ['leite', 'maçã', 'iogurte', 'arroz'];
console.log(listaCompras);
//length identificar quantidade de elementos no array
console.log(listaCompras.length)
//splice remove parte do array apartir do indice informado.
console.log(listaCompras.splice(3));
console.log(listaCompras)
//push adiciona um elemento no final do array.
console.log(listaCompras.push('Feijão'))
console.log(listaCompras)
//pop remove o último elemento de uma array.
console.log(listaCompras.pop())
//shift apaga o primeiro elemento do array.
console.log(listaCompras.shift())
console.log(listaCompras)