#include <stdio.h>

int main(){
   int numeroDeMacas = 0;

   printf("Vamos contar  o numero de macas colhidas: \n");

   while (numeroDeMacas < 10) {
    numeroDeMacas++;
    printf("Macas contadas: %d\n", numeroDeMacas);
   }
   printf("Contagem concluida! Total de macas: %d\n", numeroDeMacas);


return 0;
}
